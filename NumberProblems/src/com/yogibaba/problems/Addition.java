package com.yogibaba.problems;

public class Addition {
	
	
	static int sum  = 0;
	
	
	public static void main(String[] args) {
		
		
		for(int i=1 ; i<=10 ;i++)
		{
			
			sum= sum + i;
			
		}
		
		System.out.println("Sum of first 10 natural Numbers are :"+sum);
		
		
	}

}
